public class If {

  // Usando un if, crear una condición que compare si la variable numeroIf es
  // positivo, negativo, o 0.
  public void numeroIf() {
    int numeroIf = -5;
    if (numeroIf > 0) {
      System.out.println("El número es positivo");
    } else if (numeroIf < 0) {
      System.out.println("El número es negativo");
    }
  }

}
