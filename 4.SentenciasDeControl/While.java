public class While {

  // Crea un bucle While, este bucle tendrá que tener como condición que la
  // variable numeroWhile sea inferior a 3
  public void numeroWhile() {
    int numeroWhile = -5;
    while (numeroWhile < 3) {
      numeroWhile++;
      System.out.println(numeroWhile);
    }
  }

}
